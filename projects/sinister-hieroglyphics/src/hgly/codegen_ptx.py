from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional

from . import ir as I


PTX_VERSION = "7.8"           # CUDA 12.0+ PTX ISA version
PTX_TARGET  = "sm_89"         # RTX 4090 / Ada compute capability
PTX_ADDR    = ".address_size 64"


@dataclass
class PtxKernel:
    name: str
    params: List[str] = field(default_factory=list)
    body: List[str] = field(default_factory=list)

    def emit(self) -> str:
        param_decl = ", ".join(f".param .u64 {p}" for p in self.params)
        return (
            f".visible .entry {self.name}({param_decl})\n"
            f"{{\n"
            + "".join(f"  {line}\n" for line in self.body)
            + f"  ret;\n"
            f"}}"
        )


@dataclass
class PtxModule:
    kernels: List[PtxKernel] = field(default_factory=list)

    def emit(self) -> str:
        header = (
            f".version {PTX_VERSION}\n"
            f".target {PTX_TARGET}\n"
            f"{PTX_ADDR}\n\n"
            f"// Generated by hgly Phase 5 PTX codegen stub\n"
            f"// Author: RKOJ-ELENO :: 2026-05-25\n"
        )
        body = "\n\n".join(k.emit() for k in self.kernels)
        return header + "\n" + body + ("\n" if body else "")


# ---- IR lowering --------------------------------------------------------

_OP_TO_PTX_INSTR: Dict[str, str] = {
    "+":   "add.s64",
    "-":   "sub.s64",
    "*":   "mul.lo.s64",
    "/":   "div.s64",
    "%":   "rem.s64",
    "&&":  "and.b64",
    "||":  "or.b64",
    "==":  "setp.eq.s64",
    "!=":  "setp.ne.s64",
    "<":   "setp.lt.s64",
    ">":   "setp.gt.s64",
    "<=":  "setp.le.s64",
    ">=":  "setp.ge.s64",
}


def _fresh_reg(state: Dict[str, Any], prefix: str = "r") -> str:
    state["counter"] = state.get("counter", 0) + 1
    return f"%{prefix}{state['counter']}"


def _lower_fn(fn: I.Function) -> PtxKernel:
    k = PtxKernel(name=fn.name or "__hgly_kernel__",
                  params=[p[0] for p in fn.params])
    state: Dict[str, Any] = {"counter": 0, "regmap": {}}

    # Param load preamble: emit ld.param for each param so subsequent ops
    # can reference %r{i}. Real codegen would track types; stub uses .u64.
    for i, (name, _ty) in enumerate(fn.params):
        reg = _fresh_reg(state)
        state["regmap"][name] = reg
        k.body.append(f".reg .u64 {reg};")
        k.body.append(f"ld.param.u64 {reg}, [{name}];")

    # Walk ops, emit PTX-ish for each. UNSUPPORTED ops emit a `// skipped`
    # comment so the file stays valid + diff-friendly.
    for block in fn.blocks:
        for op in block.ops:
            if isinstance(op, I.Const):
                if op.dest_var:
                    reg = _fresh_reg(state)
                    state["regmap"][op.dest_var] = reg
                    val = op.args[0]
                    if isinstance(val, (int, float)):
                        k.body.append(f".reg .s64 {reg};")
                        k.body.append(f"mov.s64 {reg}, {int(val)};")
                    else:
                        k.body.append(f"// const non-numeric: {val!r}")
            elif isinstance(op, I.BinOp):
                lhs, rhs = op.args[1], op.args[2]
                lhs_r = state["regmap"].get(lhs, "%lit0")
                rhs_r = state["regmap"].get(rhs, "%lit1")
                if op.dest_var:
                    dst = _fresh_reg(state)
                    state["regmap"][op.dest_var] = dst
                    instr = _OP_TO_PTX_INSTR.get(op.args[0], "// unknown op")
                    if instr.startswith("setp"):
                        # comparison -> predicate; allocate a pred reg
                        k.body.append(f".reg .pred {dst};")
                        k.body.append(f"{instr} {dst}, {lhs_r}, {rhs_r};")
                    else:
                        k.body.append(f".reg .s64 {dst};")
                        k.body.append(f"{instr} {dst}, {lhs_r}, {rhs_r};")
            elif isinstance(op, I.Return):
                if op.args:
                    src_reg = state["regmap"].get(op.args[0], "%lit0")
                    k.body.append(f"// return value in {src_reg}")
                break
            elif isinstance(op, I.Call):
                # GPU kernels rarely call back to host functions; stub annotation.
                callee = op.args[0]
                k.body.append(f"// call {callee}({len(op.args) - 1} args) -- "
                              f"host-side; not lowered")
            elif isinstance(op, I.Load):
                if op.dest_var:
                    reg = _fresh_reg(state)
                    state["regmap"][op.dest_var] = reg
                    k.body.append(f".reg .s64 {reg};")
                    k.body.append(f"mov.s64 {reg}, 0; // load {op.args[0]}")
            else:
                k.body.append(f"// {op.kind} -- not lowered")
    return k


def compile_module(mod: I.Module, only_gpu: bool = False) -> PtxModule:
    """Lower IR Module to PtxModule. By default, lowers every function
    (treats them as candidate kernels). With only_gpu=True, lowers only
    functions with Effect.GPU."""
    out = PtxModule()
    fns: List[I.Function] = [mod.main] + list(mod.functions)
    for fn in fns:
        if only_gpu and fn.effect != I.Effect.GPU:
            continue
        out.kernels.append(_lower_fn(fn))
    return out


def emit(src: str) -> str:
    """Convenience: source -> ast -> ir -> ptx text."""
    from . import parse
    prog = parse(src)
    mod = I.lower_ast(prog)
    return compile_module(mod).emit()
